    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed

        String Ncode = editCode.getText();
        try {
            String sql = "select * from users u where code = '" + Ncode + "'";
            con = DBConnection.getConnection();
            ps = con.createStatement();
            rs = ps.executeQuery(sql);
            if (rs.next()) {
                fcode = rs.getString("code");
                uCode.setText(fcode);
                l1.setText(fcode);

                fname = rs.getString("name");
                uName.setText(fname);
                l2.setText(fname);

                fusername = rs.getString("user_name");
                uUname.setText(fusername);
                l3.setText(fusername);

                fpassword = rs.getString("password");
                uPassword.setText(fpassword);
                l4.setText(fpassword);

                fgender = rs.getString("gender");
                if (fgender.equals("Female")) {
                    male.setSelected(false);
                    other.setSelected(false);
                    female.setSelected(true);
                    l5.setText("Female");
                } else if (fgender.equals("Male")) {
                    female.setSelected(false);
                    other.setSelected(false);
                    male.setSelected(true);
                    l5.setText("Male");
                } else {
                    female.setSelected(false);
                    other.setSelected(true);
                    male.setSelected(false);
                    l5.setText("Other");
                }

                faddress = rs.getString("address");
                uAddress.setText(faddress);
                l6.setText(faddress);

                fphone = rs.getString("phone");
                uPhone.setText(fphone);
                l7.setText(fphone);

                femail = rs.getString("email");
                uEmail.setText(femail);
                l8.setText(femail);

            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

