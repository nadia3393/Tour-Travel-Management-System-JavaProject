    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:

        if (male.isSelected()) {

            fgender = "Male";
        } else if (female.isSelected()) {

            fgender = "Female";
        } else if (other.isSelected()) {

            fgender = "Other";
        } else {
            fgender = "";
        }

        User u = new User();
        u.setCode(uCode.getText());
        u.setName(uName.getText());
        u.setUser_name(uUname.getText());
        u.setPassword(uPassword.getText());
        u.setGender(fgender);
        u.setAddress(uAddress.getText());
        u.setPhone(uPhone.getText());
        u.setEmail(uEmail.getText());

        int status = new UserService().save(u);
        if (status == 1) {
            JOptionPane.showMessageDialog(rootPane, "Saved!");
            clear();
        } else {
            JOptionPane.showMessageDialog(rootPane, "Not Saved!");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

