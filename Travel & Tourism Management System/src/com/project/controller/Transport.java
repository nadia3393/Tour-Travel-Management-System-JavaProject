               lb3.setText(rs.getString("place"));
               
               numPerson.setText(rs.getString("person"));
               lb4.setText(rs.getString("person"));
               
               
               
               transCost.setText(rs.getString("transport_cost"));
               lb5.setText(rs.getString("transport_cost"));
               
               totalCost.setText(rs.getString("total_trans_cost"));
               lb7.setText(rs.getString("total_trans_cost"));
               
                
                Date date1 = new SimpleDateFormat("dd MMM yyyy").parse(rs.getString("date"));
