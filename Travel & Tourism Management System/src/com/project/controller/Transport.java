    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        // TODO add your handling code here:
        try {

            String sql = "update transport set code = ?,transport = ?,place = ?,person = ?,transport_cost = ?,total_trans_cost = ?, date = ? where code = ?";
            con = DBConnection.getConnection();
            ps = con.prepareStatement(sql);
            ps.setString(1, comUserCode.getSelectedItem().toString());
            ps.setString(2, comTrans.getSelectedItem().toString());
            ps.setString(3, comPlace.getSelectedItem().toString());
            ps.setInt(4, Integer.parseInt(numPerson.getText()));
            ps.setString(5, transCost.getText());
            ps.setString(6, totalCost.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("dd MMMM yyyy", Locale.getDefault());
        String d = String.valueOf(sdf.format(date.getDate())); 
            ps.setString(7,d);
            ps.setString(8,comUserCode.getSelectedItem().toString());
            
            ps.execute();
            
            JOptionPane.showMessageDialog(rootPane, "UPDATATION SUCCESSFULLY.");
            clear();
            con.close();
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_btnEditActionPerformed

